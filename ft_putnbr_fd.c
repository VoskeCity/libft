#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*c;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
		c = ft_itoa(n);
	ft_putstr_fd(c, fd);
}
